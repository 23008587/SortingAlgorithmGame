   //Method to check if 2 buttons are selected 
public void checkButtonSelection() {
    int selectedCount = 0;

    if (numbtn1.isSelected()) selectedCount++;
    if (numbtn2.isSelected()) selectedCount++;
    if (numbtn3.isSelected()) selectedCount++;
    if (numbtn4.isSelected()) selectedCount++;
    if (numbtn5.isSelected()) selectedCount++;
    if (numbtn6.isSelected()) selectedCount++;
    if (numbtn7.isSelected()) selectedCount++;
    if (numbtn8.isSelected()) selectedCount++;
    if (numbtn9.isSelected()) selectedCount++;
    if (numbtn10.isSelected()) selectedCount++;

    // If more than two buttons are selected, disable all buttons
    if (selectedCount >= 2) {
        numbtn1.setEnabled(false);
        numbtn2.setEnabled(false);
        numbtn3.setEnabled(false);
        numbtn4.setEnabled(false);
        numbtn5.setEnabled(false);
        numbtn6.setEnabled(false);
        numbtn7.setEnabled(false);
        numbtn8.setEnabled(false);
        numbtn9.setEnabled(false);
        numbtn10.setEnabled(false);
    }
}



