         int Swaps = 0;//initial swapp count
    private void SwappBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SwappBtnActionPerformed
    //Create an array of buttons
    JToggleButton[] buttons = {numbtn1, numbtn2, numbtn3, numbtn4, numbtn5, numbtn6, numbtn7, numbtn8, numbtn9, numbtn10};
    JToggleButton firstSelected = null;//create a tooglebutton
    JToggleButton secondSelected = null;
   Swaps++;
    // Find the selected buttons
    for (JToggleButton button : buttons) {//to check which button has been selected
        if (button.isSelected()) {
            if (firstSelected == null) {
                firstSelected = button;
            } else {
                secondSelected = button;
               
                break; // Stop searching after finding two selected buttons
            }
            
        }
    }

    // Swap texts if two buttons are selected
    if (firstSelected != null && secondSelected != null) {
        swaapTexts(firstSelected, secondSelected);
        firstSelected.setSelected(false);
        secondSelected.setSelected(false);
        
    }
    
    Cancel();//reset the buttons to default
    
    swaps.setText(Integer.toString(Swaps));//set text of the swap count
       
    }//GEN-LAST:event_SwappBtnActionPerformed
        boolean IsSorted(){//Method to check if the user entered sorted numbers
               return parseInt(numbtn1.getText()) < parseInt(numbtn2.getText())
                &&    parseInt(numbtn3.getText()) < parseInt(numbtn4.getText())
                &&    parseInt(numbtn5.getText()) < parseInt(numbtn6.getText())
                &&    parseInt(numbtn7.getText()) < parseInt(numbtn8.getText())
                &&    parseInt(numbtn9.getText()) < parseInt(numbtn10.getText());
           }
